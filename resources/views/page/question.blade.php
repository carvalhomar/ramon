                                Um dos diagnósticos da "moda" é o Transtorno de Humor Bipolar.
                                Frequentemente se escuta alguém que oscila de humor ser chamado de bipolar.
                                Porém, a maioria das oscilações de humor é normal, ou seja, pequenas variações são esperadas e
                                adequadas às situações que se apresentam no dia a dia. Alegria e tristeza são sentimentos que servem
