                            </p><br/>
                            <p class="curriculum-descript">
                                Depois disso faz 3 anos de residência em psiquiatria.
                                Nessa etapa ele se aprofunda no estudo da parte psíquica do indivíduo, seus conflitos,
                                o que lhe traz satisfações e sofrimento e suas possíveis doenças.
                                Aprende a fazer uso de medicações, chamadas de psicofármacos, e de psicoterapias.
                            </p><br/>
                            <p class="curriculum-descript">
                                Numa etapa seguinte, que leva de 1 a 2 anos, pode ainda se subespecializar,
                                fazendo nova residência médica na área de psiquiatria da infância e adolescência,
                                psicogeriatria, psiquiatria forense ou psicoterapia. Se sua área for a de psicoterapia,
                                aprofunda seus conhecimentos nas interações entre os aspectos psíquicos
                                e as diversas áreas de expressão humana, em situações que envolvem ou não doença.
                            </p><br/>
                            <p class="curriculum-descript">
                                <b>2)</b> O psicólogo é o profissional que faz 5 anos de faculdade de psicologia. Passa pelas áreas de psicologia organizacional,
                                psicologia clínica, psicologia social etc. Isso possibilita a ele entender a interação dos aspectos
                                psíquicos com as diversas áreas de expressão humana, seja a escola, o trabalho,
                                os esportes, os relacionamentos interpessoais e outros.
                            </p><br/>
                            <p class="curriculum-descript">
                                Ele pode fazer especialização, de até 3 anos, em uma dessas diversas áreas,
                                aprofundando seu conhecimento. Se sua opção for pela área clínica,
                                especializa-se em psicoterapia, que pode ser da infância e adolescência ou do adulto e idoso.
                            </p><br/>
                            <p class="curriculum-descript">
                                <b>3)</b> A psicanálise não é uma profissão regulamentada pelo Ministério do Trabalho,
                                ou seja, não existe regulamentação que indique ou impeça que alguém se anuncie como
                                psicanalista, mesmo sem qualquer formação nessa área ou com uma formação mínima.
                                Existem diversas escolas e instituições de psicanálise no Brasil e no mundo.
                                Uma das principais diferenças entre elas é se são ou não vinculadas à
                                <i>International Psychoanalytical Association</i> (Associação Psicanalítica Internacional – IPA).
                                Essa instituição é atualmente sediada em Londres, foi criada por Freud e seus seguidores e
                                mostra-se bastante criteriosa na formação dos psicanalistas.
                            </p><br/>
                            <p class="curriculum-descript">
                                No Brasil existem atualmente 13 sociedades de psicanálise vinculadas à IPA.
                                No Rio Grande do Sul são três: a Sociedade Psicanalítica de Porto Alegre (SPPA),
                                a Sociedade Brasileira de Psicanálise de Porto Alegre (SBPdePA) e a
                                Sociedade Psicanalítica de Pelotas (SPPel). Santa Catarina e Paraná ainda não têm sociedades vinculadas à IPA.
                            </p><br/>
                            <p class="curriculum-descript">
                                Os psicanalistas formados pela IPA normalmente são médicos ou psicólogos já com experiência em psicoterapias
                                que passam por uma formação adicional, composta por seminários teóricos de duração média de 4 anos,
                                supervisões de casos de análise de alta frequência (de 3, 4 ou mais sessões por semana) e uma extensa análise pessoal.
                            </p>
