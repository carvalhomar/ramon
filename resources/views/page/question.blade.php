                                A maioria das medicações psiquiátricas não causa dependência, tanto física quanto psíquica.
                                Porém, muitas vezes há necessidade do uso contínuo de medicações por outras situações.
                                Algumas doenças, como esquizofrenia e transtorno do humor bipolar, por exemplo, necessitam de contínua
                                regulação química para se manterem estáveis. Em outras condições psíquicas, seja por um quadro de longa
                                data ou uma incapacidade ou não disponibilidade de tratamento psicoterápico, também há necessidade de uso contínuo de medicações.
                                Porém, isso se deve à impossibilidade de um tratamento efetivo da doença de base, e não à dependência dos medicamentos.
                            </p><br>
                            <p class="curriculum-descript">
                                Os benzodiazepínicos (diazepam, alprazolam, clonazepam, lorazepam e outros) são uma exceção, ou seja,
                                podem causar tolerância (necessidade de maior doses para se obter o mesmo efeito) e dependência (necessidade constante do uso para não haver sintomas de abstinência).
                                Tais manifestações ocorrem principalmente no caso de uso contínuo por muito tempo (meses ou anos) e se o uso for paliativo,
                                ou seja, quando se tenta manejar sintomas ansiosos e depressivos sem fazer o tratamento adequado com medicações melhores e mais seguras.
