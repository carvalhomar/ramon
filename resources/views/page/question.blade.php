                            <p class="curriculum-descript">
                                <b>1)</b> O psiquiatra é o profissional que faz 6 anos de faculdade de medicina. Passa pelas áreas básicas,
                                clínicas e cirúrgicas, incluindo patologia, farmacologia, clínica médica,
                                cardiologia, neurologia, gastroenterologia etc. Isso possibilita a ele uma visão do indivíduo como um todo,
