                                    <p class="curriculum-descript">
                                        <b>Desabafar:</b> ao contrário do que algumas pessoas pensam,
                                        a melhora não acontece porque a pessoa "desabafou" ou "jogou para fora"
                                        o que a incomodava. Evidentemente que poder falar sobre o que perturba
                                        traz alívio imediato. Porém, se o porquê e o para que do fator perturbador
