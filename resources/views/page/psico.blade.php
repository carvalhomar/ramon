                                    <p class="curriculum-descript">
                                        A psicanálise pode ser entendida de pelo menos duas maneiras: ela pode ser um método de autoconhecimento,
                                        através do qual, junto ao analista, o indivíduo pode conhecer-se profundamente; e é também um
                                        método terapêutico, ou seja, uma forma de psicoterapia.
                                    </p><br/>
                                    <p class="curriculum-descript">
                                        Diferentemente de outras psicoterapias, não existe um objetivo circunscrito, e todas as vivências do dia a dia,
                                        lembranças do passado e expectativas para o futuro têm espaço livre para a reflexão.
                                        As manifestações internas (imaginações, sonhos, expectativas) são tão valorizadas quanto as
                                        externas (relacionamentos, experiências, ações).
                                    </p><br/>
                                    <p class="curriculum-descript">
                                        O pressuposto básico da psicanálise é o de que o universo íntimo, na sua maioria inconsciente,
                                        determina a forma como entendemos e conduzimos nossas vidas
                                        (a forma como nos relacionamos com os outros e com nós mesmos, nossa capacidade de trabalhar e de usufruir da vida),
                                        mesmo sem nos darmos conta dos porquês de nossas escolhas, facilidades e dificuldades.
                                    </p><br/>
                                    <p class="curriculum-descript">
                                        Quanto mais temos consciência desse universo íntimo e de suas leis (às vezes aparentemente ilógicas),
                                        mais donos de nossas vidas somos (mais livres e autônomos).
                                        Assim, a psicanálise é um caminho para as pessoas que buscam não apenas a cessação do sofrimento e o
                                        restabelecimento um modo de ser perdido com uma doença ou situação externa, mas também ir além dos seus tetos
                                        ou fronteiras presentes.
                                    </p><br/>
                                    <p class="curriculum-descript">
                                        Não existe uma frequência mínima para se pensar sobre si (fazer análise), mas encontros mais próximos
                                        (3, 4, 5 ou mais sessões na semana) possibilitam pensar sobre as situações acontecendo ao vivo
                                        (e não apenas as que já aconteceram), com a emoção presente e com maior possibilidade de autodescobrimentos mais
                                        profundos. Deixa-se de apenas relatar e refletir teoricamente para vivenciar as situações e refletir
                                        afetivamente sobre elas.
                                    </p><br/>
                                    <p class="curriculum-descript">
                                        As ferramentas para esse processo são a disposição da pessoa para relatar o
                                        mais livremente possível todos os seus pensamentos e sentimentos e a
                                        capacidade de receptividade e de conexão do psicanalista.
                                        Nesse contexto, pode-se também fazer uso do divã, que tem como propósito
                                        permitir à pessoa lá deitada estar a sós consigo mesma e ao mesmo tempo ser
                                        acompanhada por outro que a ouve.
