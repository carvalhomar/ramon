                                        O trabalho do terapeuta é ajudar a compreender as circunstâncias internas e
                                        externas que mantêm aquilo que traz sofrimento, e para isso precisa
                                        de uma escuta atenta e por vezes ficar em silêncio. Porém, tal silêncio
