                        <h2 class="title" style="color:#ffffff;">Farmacoterapia</h2>
                        <p style="color:#ffffff;" class="section-subtitle mx-auto">Qual é o princípio do uso de medicações</p>
                        <br/>
                        <img style="float: right; padding-left:35px;" src="assets/images/farmacoterapia_ramon.jpeg" />
                        <p class="curriculum-descript" style="color:#ffffff;">
                            Existem algumas substâncias químicas chamadas neurotransmissores,
                            que fazem a comunicação entre um neurônio e outro. Exemplos são:
                            serotonina, noradrenalina, dopamina, GABA, glutamato e outros.
                        </p>  <br/>
                        <p class="curriculum-descript" style="color:#ffffff;">
                            As medicações psiquiátricas agem na regulação desses neurotransmissores,
                            aumentando quando estão baixos ou diminuindo quando estão elevados patologicamente.
                        </p><br/>
                        <p class="curriculum-descript" style="color:#ffffff;">
                            Além disso, existem algumas outras medicações que agem na transmissão elétrica ao longo
                            dos neurônios. Quando bem indicadas tais medicações auxiliam no reequilíbrio bioquímico
                            cerebral e, por conseguinte, no fortalecimento de conexões "saudáveis".
                            Em algumas doenças como esquizofrenia e transtorno de humor bipolar, por exemplo,
                            as medicações também evitam um processo de dano bioquímico neuronal progressivo,
                            que ocorreria se a doença fosse deixada "solta".
                        </p><br/>
                        <p class="curriculum-descript" style="color:#ffffff;">
                            Contrariamente ao senso comum, que considera o uso de medicações arriscado e
                            até danoso em um cérebro em desenvolvimento (como nas crianças e adolescentes),
                            o uso correto e bem indicado é benéfico e favorece um melhor desenvolvimento do cérebro.
                        </p><br/>
                        <p class="curriculum-descript" style="color:#ffffff;">
                            Como nas doenças que acometem outras áreas do corpo, as doenças psiquiátricas,
                            se deixadas sem tratamento, tendem a uma evolução pior, ou seja,
                            demoram mais para melhorar e/ou deixam sequelas.
                        </p><br/>
